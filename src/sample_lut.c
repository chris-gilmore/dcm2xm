u32 get_sample_size(u16 smp_id) {
  u32 size;

  FILE *fp = fopen(sample_lut[smp_id][1], "rb");
  if (fp == NULL) exit(1);

  fseek(fp, 0, SEEK_END);
  size = ftell(fp);
  fclose(fp);

  return size;
}

void load_sample(void *buf, u16 smp_id) {
  u32 size;

  FILE *fp = fopen(sample_lut[smp_id][1], "rb");
  if (fp == NULL) exit(1);

  fseek(fp, 0, SEEK_END);
  size = ftell(fp);
  rewind(fp);

  fread(buf, 1, size, fp);
  fclose(fp);

  printf("fname: %s | size: %d 0x%X\n", sample_lut[smp_id][1], size, size);
}
